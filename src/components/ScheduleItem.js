import React from 'react';
import { getTypeColor, calculateDuration } from '../utils.js';

const ScheduleItem = ({ item, index, onEdit, onDelete, onPomodoroClick }) => {
  const duration = calculateDuration(item.start_time, item.end_time);
  const isPomodoro = item.details && item.details.toLowerCase().includes('pomodoro');
  const isAutoGenerated = item.isAutoGenerated;
  const isNewlyAdded = item.isNewlyAdded;

  return (
    <div 
      className={`schedule-item relative p-3 mb-3 rounded border-l-4 
                 ${getTypeColor(item.type)} 
                 shadow-sm transition-all duration-200 hover:shadow-md cursor-grab
                 ${isAutoGenerated ? 'ring-2 ring-purple-300 animate-pulse-purple' : ''}
                 ${isNewlyAdded ? 'animate-slide-in ring-2 ring-green-300' : ''}
                 focus-within:ring-2 focus-within:ring-blue-500`}
      tabIndex="0"
      aria-label={`${item.activity} from ${item.start_time} to ${item.end_time}`}
      role="article"
    >
      <div className="flex justify-between items-start mb-1 flex-wrap">
        <div className="flex items-center flex-wrap">
          <span className="font-semibold text-gray-800 mr-2">
            {item.activity}
            {isAutoGenerated && (
              <span className="ml-2 inline-flex items-center px-2 py-0.5 rounded text-xs font-medium bg-purple-100 text-purple-800">
                Auto
              </span>
            )}
          </span>
          <div className="inline-flex mr-2">
            <button 
              className="action-btn btn-edit mr-1 transition-colors" 
              title="Edit"
              aria-label={`Edit ${item.activity}`}
              onClick={(e) => {
                e.stopPropagation();
                e.preventDefault();
                onEdit(index);
              }}
            >
              &#x270E;
            </button>
            {isPomodoro && (
              <button 
                className="action-btn btn-pomodoro transition-colors" 
                title="Show Pomodoro Timer"
                aria-label={`Start Pomodoro timer for ${item.activity}`}
                onClick={(e) => {
                  e.stopPropagation();
                  e.preventDefault();
                  onPomodoroClick(index);
                }}
              >
                P
              </button>
            )}
          </div>
        </div>
        <span className="text-xs font-mono bg-gray-200 px-2 py-0.5 rounded text-gray-700 whitespace-nowrap">
          {item.start_time} - {item.end_time}
        </span>
      </div>
      <div className="text-sm text-gray-600 flex justify-between items-center flex-wrap">
        <span className="mr-2 details-text">
          {item.details ? (
            <span dangerouslySetInnerHTML={{ 
              __html: item.details.replace(/Pomodoro[s]?/gi, '<strong class="text-indigo-600 font-semibold">$&</strong>') 
            }} />
          ) : (
            <i className="text-gray-400">No details</i>
          )}
        </span>
        <div className="ml-auto flex items-center">
          <span className="text-xs text-gray-500 font-medium whitespace-nowrap mr-2">
            ({duration})
          </span>
          <button 
            className="action-btn btn-delete transition-colors" 
            title="Delete"
            aria-label={`Delete ${item.activity}`}
            onClick={(e) => {
              e.stopPropagation();
              e.preventDefault();
              onDelete(index);
            }}
          >
            &#x1F5D1;
          </button>
        </div>
      </div>
    </div>
  );
};

export default ScheduleItem;